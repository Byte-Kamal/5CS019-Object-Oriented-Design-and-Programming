package tutorial;

public class DataTypes {
	public static void main(String[] args) {
		
		// whole number data types
		byte b = 120;
		short s = 1290;
		int i = 13523;
		long l = 3522346232l;
		
		System.out.println("Byte data types with value: " + b);
		System.out.println("Short data types with value: " + s);
		System.out.println("Int data types with value: " + i);
		System.out.println("Long data types with value: " + l);
		System.out.println("-----------------------------------------");

		
		// decimal number data types
		float f = 24.53f;
		double d = 53.24;
		
		System.out.println("Float data types with value: " + f);
		System.out.println("Double data types with value: " + d);
	}
}
