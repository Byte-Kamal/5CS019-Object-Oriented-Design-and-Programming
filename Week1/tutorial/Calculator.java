package tutorial;

public class Calculator {

	public static void main(String[] args) {
		
		int firstNumber = 20;
		int secondNumber = 5;
		int addition = firstNumber + secondNumber;
		int subtraction = firstNumber - secondNumber;
		int multiplication = firstNumber * secondNumber;
		int division = firstNumber / secondNumber;
		
		System.out.println("Addition of 20 and 5 is: " + addition);
		System.out.println("Subtraction of 20 and 5 is: " + subtraction);
		System.out.println("Multiplication of 20 and 5 is: " + multiplication);
		System.out.println("Division of 20 and 5 is: " + division);
	}

}
